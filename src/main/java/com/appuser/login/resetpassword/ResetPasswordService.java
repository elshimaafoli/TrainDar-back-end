    public String validEmailToken(String email, String token) {
        ConfirmationToken confirmationToken = confirmationTokenService.getToken(token)
                .orElseThrow(() -> new IllegalStateException("token not found"));
        if (!confirmationToken.getAppUser().getEmail().equals(email)) {
            throw new IllegalStateException("email or wrong code");
        }
        if (confirmationToken.getConfirmedAt() != null) {
            throw new IllegalStateException("code already confirmed");
        }
        LocalDateTime expiredAt = confirmationToken.getExpiresAt();
        if (expiredAt.isBefore(LocalDateTime.now())) {
            throw new IllegalStateException("code expired");
        }
        confirmationTokenService.setConfirmedAt(token);
        return "valid email and token";
    }

    public String validEmailPassword(ResetPasswordRequest resetPasswordRequest) {
        boolean userExists = appUserRepository.findByEmail(resetPasswordRequest.getEmail()).isPresent();
        if (!userExists) {
            throw new IllegalStateException("no such email");
        }
        String encryptPassword = bcryptPasswordEncoder.encode(resetPasswordRequest.getPassword());
        appUserRepository.updatePassword(resetPasswordRequest.getEmail(), encryptPassword);
        return "Password changed successfully";
    }
